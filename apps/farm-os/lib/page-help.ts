/** Returns the help for a nav id, or null if none is defined. */
export function helpFor(navId: string): PageHelp | null {
  return PAGE_HELP[navId] ?? null;
}

/** Returns route-specific help for 360/detail routes, falling back to the active nav page. */
export function helpForPath(pathname: string, fallbackHelpId: string): PageHelp | null {
  const routeHelp = ROUTE_HELP.find((entry) => entry.pattern.test(pathname));
  return helpFor(routeHelp?.helpId ?? fallbackHelpId);
}
